use crossterm::{
	cursor::Show,
	execute,
	terminal::{
		Clear,
		ClearType,
	},
};
use super::{
	SetForegroundColour,
	Colour,
	log,
	stdout,
};
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Print the reset ansi sequence.
pub(crate) fn exit() {
	print!("\r");
	if let Err(why) = execute!(stdout(),
		Show,
		SetForegroundColour(Colour::Reset),
	) { log!(err: "reset the terminal style" => why) }
	print!("\0")
}

/// Print the clear line sequence.
pub(crate) fn clear() {
	print!("\r");
	execute!(stdout(), Clear(ClearType::CurrentLine)).expect("clear the current line");
	print!("\n\n\0")
}
